''' 
variables are the basic building blocks of all software
We can store all kinds of data in variables
to create a variable, simply type: <the name of the variable> = <value>
'''

# these are variables, and the data type is an Integer
# an Integer is a whole number in programming
apples = 5
oranges = 10
print('Apples: ', apples)
print('Oranges: ', oranges)

# this is a variable with a FLOAT data type
# float is a number with a decimal
gpa = 3.17
print('GPA: ', gpa)

# this is a string. In programing, a string is just text.
firstName = 'John'
lastName = 'Doe'
print('First Name: ', firstName)
print('Last Name: ', lastName)